use uefi::proto::console::gop::GraphicsOutput;
use uefi::boot;

pub struct PixelGraphics {
    framebuffer: *mut u32,
    width: usize,
    height: usize,
    stride: usize,
}

impl PixelGraphics {
    pub fn new() -> Option<Self> {
        let gop_handle = boot::get_handle_for_protocol::<GraphicsOutput>().ok()?;
        let mut gop = boot::open_protocol_exclusive::<GraphicsOutput>(gop_handle).ok()?;

        let mode_info = gop.current_mode_info();
        let (width, height) = mode_info.resolution();
        let stride = mode_info.stride();
        let mut fb = gop.frame_buffer();
        let fb_ptr = fb.as_mut_ptr() as *mut u32;

        Some(Self {
            framebuffer: fb_ptr,
            width,
            height,
            stride,
        })
    }

    #[inline]
    pub fn draw_pixel(&mut self, x: usize, y: usize, color: u32) {
        if x < self.width && y < self.height {
            unsafe {
                *self.framebuffer.add(y * self.stride + x) = color;
            }
        }
    }

    pub fn fill_rect(&mut self, x: usize, y: usize, width: usize, height: usize, color: u32) {
        for py in y..y + height {
            for px in x..x + width {
                self.draw_pixel(px, py, color);
            }
        }
    }

    pub fn draw_line(&mut self, x1: usize, y1: usize, x2: usize, y2: usize, color: u32) {
        let dx = (x2 as i32 - x1 as i32).abs();
        let dy = (y2 as i32 - y1 as i32).abs();
        let sx = if x1 < x2 { 1 } else { -1 };
        let sy = if y1 < y2 { 1 } else { -1 };
        let mut err = dx - dy;

        let mut x = x1 as i32;
        let mut y = y1 as i32;

        loop {
            self.draw_pixel(x as usize, y as usize, color);
            if x == x2 as i32 && y == y2 as i32 { break; }
            let e2 = 2 * err;
            if e2 > -dy {
                err -= dy;
                x += sx;
            }
            if e2 < dx {
                err += dx;
                y += sy;
            }
        }
    }

    pub fn draw_char(&mut self, x: usize, y: usize, c: char, color: u32) {
        let font_data = get_font_data(c);
        for row in 0..16 {
            for col in 0..8 {
                if (font_data[row] & (1 << (7 - col))) != 0 {
                    self.draw_pixel(x + col, y + row, color);
                }
            }
        }
    }

    pub fn draw_text(&mut self, x: usize, y: usize, text: &str, color: u32) {
        let mut curr_x = x;
        for c in text.chars() {
            self.draw_char(curr_x, y, c, color);
            curr_x += 8;
        }
    }

    pub fn clear(&mut self, color: u32) {
        self.fill_rect(0, 0, self.width, self.height, color);
    }

    pub fn resolution(&self) -> (usize, usize) {
        (self.width, self.height)
    }
}

