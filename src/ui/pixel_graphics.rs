use uefi::proto::console::gop::GraphicsOutput;
use uefi::boot;
use core::ptr;

pub struct PixelGraphics {
    framebuffer: *mut u32,
    width: usize,
    height: usize,
    stride: usize,
    backbuffer: Option<alloc::vec::Vec<u32>>,
}

static mut GOP_CACHE: Option<( *mut u32, usize, usize, usize)> = None;

impl PixelGraphics {
    pub fn new() -> Option<Self> {
        let (fb_ptr, width, height, stride) = unsafe {
            if let Some(cache) = GOP_CACHE {
                cache
            } else {
                let gop_handle = boot::get_handle_for_protocol::<GraphicsOutput>().ok()?;
                let mut gop = boot::open_protocol_exclusive::<GraphicsOutput>(gop_handle).ok()?;

                let mode_info = gop.current_mode_info();
                let (w, h) = mode_info.resolution();
                let s = mode_info.stride();
                let mut fb = gop.frame_buffer();
                let ptr = fb.as_mut_ptr() as *mut u32;
                
                GOP_CACHE = Some((ptr, w, h, s));
                (ptr, w, h, s)
            }
        };

        Some(Self {
            framebuffer: fb_ptr,
            width,
            height,
            stride,
            backbuffer: None,
        })
    }

    pub fn with_backbuffer(mut self) -> Self {
        let size = self.stride * self.height;
        let mut buffer = alloc::vec::Vec::with_capacity(size);
        unsafe {
            buffer.set_len(size);
            ptr::write_bytes(buffer.as_mut_ptr(), 0, size);
        }
        self.backbuffer = Some(buffer);
        self
    }

    pub fn flip(&mut self) {
        if let Some(ref buffer) = self.backbuffer {
            unsafe {
                ptr::copy_nonoverlapping(buffer.as_ptr(), self.framebuffer, self.stride * self.height);
            }
        }
    }

    #[inline]
    pub fn draw_pixel(&mut self, x: usize, y: usize, color: u32) {
        if x < self.width && y < self.height {
            if let Some(ref mut buffer) = self.backbuffer {
                buffer[y * self.stride + x] = color;
            } else {
                unsafe {
                    *self.framebuffer.add(y * self.stride + x) = color;
                }
            }
        }
    }

    pub fn fill_rect(&mut self, x: usize, y: usize, width: usize, height: usize, color: u32) {
        for py in y..y + height {
            for px in x..x + width {
                self.draw_pixel(px, py, color);
            }
        }
    }

    pub fn draw_line(&mut self, x1: usize, y1: usize, x2: usize, y2: usize, color: u32) {
        let dx = (x2 as i32 - x1 as i32).abs();
        let dy = (y2 as i32 - y1 as i32).abs();
        let sx = if x1 < x2 { 1 } else { -1 };
        let sy = if y1 < y2 { 1 } else { -1 };
        let mut err = dx - dy;

        let mut x = x1 as i32;
        let mut y = y1 as i32;

        loop {
            self.draw_pixel(x as usize, y as usize, color);
            if x == x2 as i32 && y == y2 as i32 { break; }
            let e2 = 2 * err;
            if e2 > -dy {
                err -= dy;
                x += sx;
            }
            if e2 < dx {
                err += dx;
                y += sy;
            }
        }
    }

    pub fn draw_char(&mut self, x: usize, y: usize, c: char, color: u32) {
        let font_data = get_font_data(c);
        for row in 0..16 {
            for col in 0..8 {
                if (font_data[row] & (1 << (7 - col))) != 0 {
                    self.draw_pixel(x + col, y + row, color);
                }
            }
        }
    }

    pub fn draw_text(&mut self, x: usize, y: usize, text: &str, color: u32) {
        let mut curr_x = x;
        for c in text.chars() {
            self.draw_char(curr_x, y, c, color);
            curr_x += 8;
        }
    }

    pub fn clear(&mut self, color: u32) {
        self.fill_rect(0, 0, self.width, self.height, color);
    }

    pub fn draw_cursor(&mut self, x: usize, y: usize) {
        let color = 0xFFFFFF;
        let outline = 0x000000;
        
        // Simple arrow cursor
        for i in 0..12 {
            for j in 0..i {
                self.draw_pixel(x + j, y + i, color);
            }
            self.draw_pixel(x + i, y + i, outline);
            self.draw_pixel(x, y + i, outline);
        }
        for j in 0..12 {
            self.draw_pixel(x + j, y + 12, outline);
        }
    }

    pub fn resolution(&self) -> (usize, usize) {
        (self.width, self.height)
    }
}

